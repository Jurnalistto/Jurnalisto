export function getBeritaBySlug(slug) {
  return berita.find((item) => item.slug === slug);
}

export function getFeaturedBerita() {
  return berita.filter((item) => item.featured);
}

export function getLatestBerita() {
  return [...berita].sort((a, b) => new Date(b.tanggal) - new Date(a.tanggal));
}

export function getBeritaByKategori(kategori) {
  return berita.filter((item) => item.kategori === kategori);
}

export function getBeritaByDaerah(daerah) {
  return berita.filter((item) => item.daerah === daerah);
}

